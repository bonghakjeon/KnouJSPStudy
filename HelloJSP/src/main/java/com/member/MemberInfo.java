package com.member;

public class MemberInfo {
	private String name;
	private int age;
	
	public String getName() {  // name 속성의 getter 메서드 
		return name;
	}
	
	public void setName(String name) {  // name 속성의 setter 메서드 
		this.name = name;
	}
	
	public int getAge() {  // age 속성의 getter 메서드 
		return age;
	}
	
	public void setAge(int age) {  // age 속성의 setter 메서드 
		this.age = age;
	}
}
